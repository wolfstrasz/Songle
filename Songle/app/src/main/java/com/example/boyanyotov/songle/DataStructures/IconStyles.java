public class IconStyles {
    private String scale;
    private String link;

    public IconStyles(String scale, String link) {
        this.scale = scale;
        this.link = link;
    }

    public String getScale() {
        return scale;
    }

    public void setScale(String scale) {
        this.scale = scale;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
